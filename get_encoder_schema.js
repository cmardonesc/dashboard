try {
  const envContent = fs.readFileSync('.env', 'utf8');
  envContent.split(/\r?\n/).forEach(line => {
    const parts = line.split('=');
    if (parts.length >= 2) {
      const key = parts[0].trim();
      const val = parts.slice(1).join('=').trim().replace(/['"]/g, '');
      if (key === 'VITE_SUPABASE_URL') supabaseUrl = val;
      if (key === 'VITE_SUPABASE_ANON_KEY') supabaseKey = val;
    }
  });
} catch(e) {}

async function main() {
  try {
    const response = await axios.get(`${supabaseUrl}/rest/v1/`, {
      headers: {
        'apikey': supabaseKey,
        'Authorization': `Bearer ${supabaseKey}`
      }
    });
    
    const definitions = response.data.definitions;
    const key = Object.keys(definitions).find(k => k.toLowerCase().includes('encoder'));
    if (key) {
      console.log(`Found table definition for "${key}":`);
      console.log(JSON.stringify(definitions[key], null, 2));
    } else {
      console.log('No encoder table definition found.');
      console.log('Available tables:', Object.keys(definitions));
    }
  } catch (error) {
    console.error('Error fetching schema:', error.message);
  }
}

main();
